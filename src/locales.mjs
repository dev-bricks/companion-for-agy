export function getMessage(key, lang, placeholders = {}) {
  const translations = LOCALES[lang] || LOCALES.en;
  let msg = translations[key] || LOCALES.en[key] || '';
  for (const [name, val] of Object.entries(placeholders)) {
    msg = msg.replace(new RegExp(`\\{${name}\\}`, 'g'), () => String(val));
  }
  return msg;
}

function normalizeLocale(value) {
  if (!value || typeof value !== 'string') return null;
  const norm = value.trim().split('.')[0].replace('_', '-').toLowerCase();
  if (!norm) return null;
  if (norm === 'zh' || norm === 'zh-cn' || norm === 'zh-hans' || norm.startsWith('zh-')) return 'zh-Hans';
  const langCode = norm.split('-')[0];
  return SUPPORTED_LOCALES.find(locale => locale.toLowerCase() === langCode) || null;
}

export function detectLocale(langOption = null, env = process.env) {
  const explicitLocale = normalizeLocale(langOption);
  if (explicitLocale) return explicitLocale;

  for (const value of [env.LC_ALL, env.LC_MESSAGES, env.LANG]) {
    const envLocale = normalizeLocale(value);
    if (envLocale) return envLocale;
  }

  return 'en';
}
